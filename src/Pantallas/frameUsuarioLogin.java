    private void btningresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btningresarActionPerformed
        try {
            DefaultTableModel modelo;
            funTrabajador func = new funTrabajador();
            consTrabajador dts = new consTrabajador();
            
            dts.setLogin(txtusuario.getText());
            dts.setPassword(txtpassword.getText());
            
            modelo=func.login(dts.getLogin(), dts.getPassword());
            
            tablalistado.setModel(modelo);
            
            //si el usuario existe, si es mayor que cero, el formulario se cierra
            if (func.totalregistros > 0) {
                this.dispose();
                frameInicio form = new frameInicio();
                form.toFront();//entonces muestra la pantalla de inicio
                form.setVisible(true);
                
                //inserta en al tabla los datos de los jlabel
                frameInicio.lbidpersona.setText(tablalistado.getValueAt(0, 0).toString());
                frameInicio.lbnombre.setText(tablalistado.getValueAt(0, 1).toString());
                frameInicio.lbprimer_apellido.setText(tablalistado.getValueAt(0, 2).toString());
                frameInicio.lbsegundo_apellido.setText(tablalistado.getValueAt(0, 3).toString());
                frameInicio.lbacceso.setText(tablalistado.getValueAt(0, 4).toString());
                
                //si no es administrador... no vera las pesta√±as de archivo ni configuracion. Ya las he puest
                //como public y estatic en propiedades
                if (!frameInicio.lbacceso.getText().equals("Administrador")) {
                    frameInicio.menuarchivo.setEnabled(false);
                    frameInicio.menuconfiguraciones.setEnabled(false);
                }
                
            }else{
                JOptionPane.showMessageDialog(rootPane, "Acceso denegado", "Acceso al sistema", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btningresarActionPerformed

