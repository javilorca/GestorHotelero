    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        if (txtnombre.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "No has ingresado ningun nombre para el cliente");
            txtnombre.requestFocus();
            return;
        }

        if (txtprimer_apellido.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes indicar un apellido para el cliente");
            txtprimer_apellido.requestFocus();
            return;
        }
        
        if (txtsegundo_apellido.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes indicar un apellido para el cliente");
            txtsegundo_apellido.requestFocus();
            return;
        }

        if (txtnum_documento.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes indicar un numero de documento");
            txtnum_documento.requestFocus();
            return;
        }
        if (txtcodigo_cliente.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un codigo para el cliente");
            txtcodigo_cliente.requestFocus();
            return;
        }

        consCliente dts = new consCliente();
        funCliente func = new funCliente();

        dts.setNombre(txtnombre.getText());
        dts.setPrimer_apellido(txtprimer_apellido.getText());
        dts.setSegundo_apellido(txtsegundo_apellido.getText());
        
        int seleccionado = cbtipo_documento.getSelectedIndex();
        dts.setTipo_documento((String) cbtipo_documento.getItemAt(seleccionado));
        dts.setNum_documento(txtnum_documento.getText());
        dts.setDireccion(txtdireccion.getText());
        dts.setTelefono(txttelefono.getText());
        dts.setEmail(txtemail.getText());
        dts.setCodigo_cliente(txtcodigo_cliente.getText());
           //si la accion es guardar hace lo siguiente:
        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El cliente se registro correctamente");
                mostrar("");
                inhabilitar();
            }
        }
        //si si editase haria lo siguiente:
        else if(accion.equals("editar")){
            dts.setIdpersona(Integer.parseInt(txtidpersona.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El ciente se modifico correctamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

