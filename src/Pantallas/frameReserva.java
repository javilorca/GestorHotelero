    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        if (txtidhabitacion.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Seleccione habitación");
            txtidhabitacion.requestFocus();
            return;
        }

        if (txtidcliente.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Indique un cliente");
            txtidcliente.requestFocus();
            return;
        }

        if (txtprecio_alojamiento.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Indique precio de reserva");
            txtprecio_alojamiento.requestFocus();
            return;
        }

        consReserva dts = new consReserva();
        funReserva func = new funReserva();

        dts.setIdhabitacion(Integer.parseInt(txtidhabitacion.getText()));
        dts.setIdcliente(Integer.parseInt(txtidcliente.getText()));
        dts.setIdtrabajador(idusuario);
        

        int seleccionado = cbtipo_reserva.getSelectedIndex();
        dts.setTipo_reserva((String) cbtipo_reserva.getItemAt(seleccionado));
        
        Calendar cal;
        int dia, mes, anyo;
        cal=dcfecha_reserva.getCalendar();
        dia=cal.get(Calendar.DAY_OF_MONTH);
        mes=cal.get(Calendar.MONTH);
        anyo=cal.get(Calendar.YEAR) -1900; //para tener un formato en condiciones
        dts.setFecha_reserva(new Date(anyo, mes, dia));
        
        cal=dcfecha_entrada.getCalendar();
        dia=cal.get(Calendar.DAY_OF_MONTH);
        mes=cal.get(Calendar.MONTH);
        anyo=cal.get(Calendar.YEAR) -1900;
        dts.setFecha_entrada(new Date(anyo, mes, dia));
        
        cal=dcfecha_salida.getCalendar();
        dia=cal.get(Calendar.DAY_OF_MONTH);
        mes=cal.get(Calendar.MONTH);
        anyo=cal.get(Calendar.YEAR) -1900;
        dts.setFecha_salida(new Date(anyo, mes, dia));
        
        dts.setPrecio_alojamiento(Double.parseDouble(txtprecio_alojamiento.getText()));
        seleccionado = cbestado.getSelectedIndex();
        dts.setEstado((String) cbestado.getItemAt(seleccionado));
        
        
        

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La reserva se guardo correctamente");
                mostrar("");
                inhabilitar();
                
                
                //ocupamos la habitacion despues de realizar una reserva
               /* funHabitacion func3 = new funHabitacion();
                consHabitacion dts3 = new consHabitacion();
                
                dts3.setIdhabitacion(Integer.parseInt(txtidhabitacion.getText()));
                func3.ocuparHabitacion(dts3);*/
            }
        }
        else if(accion.equals("editar")){
            dts.setIdreserva(Integer.parseInt(txtidreserva.getText()));
            dts.setIdtrabajador(Integer.parseInt(txtidtrabajador.getText()));//para saber que trabajador lo hizo
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La reserva se modifico correctamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

